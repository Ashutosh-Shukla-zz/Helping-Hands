    private void assignJButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_assignJButtonMouseClicked

        int selectedRow = workRequestJTable1.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }

        CSRActivityWorkRequest request = (CSRActivityWorkRequest) workRequestJTable1.getValueAt(selectedRow, 0);
        // request.setNumberOfVolunteersRequired(request.getNumberOfVolunteersRequired()-1);
        CSRActivityWorkRequest csrWorkReq1;
        for (WorkRequest request1 : organization.getWorkQueue().getWorkRequestList()) {

            csrWorkReq1 = (CSRActivityWorkRequest) request1;
            for (UserAccount user : csrWorkReq1.getVolunteersList()) {
                if (user.getUsername() == userAccount.getUsername()) {
                    JOptionPane.showMessageDialog(null, "You have already assigned this CSR request to you.");
                    return;
                }
            }
        }
        request.setNumberOfVolunteersAccepted(request.getNumberOfVolunteersAccepted() + 1);
        request.setReceiver(userAccount);
        request.getVolunteersList().add(userAccount);
        if (request.getNumberOfVolunteersRequired() - request.getNumberOfVolunteersAccepted() == 0) {
            request.setStatus("Completed");
        }

        populateTable();
        populateAssignedTable();
    }//GEN-LAST:event_assignJButtonMouseClicked


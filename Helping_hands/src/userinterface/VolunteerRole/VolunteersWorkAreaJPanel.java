    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable1.getModel();

        model.setRowCount(0);
        CSRActivityWorkRequest csrWorkReq;
        for (Network network : business.getNetworkList()) {
            for (Enterprise ent : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (ent.getName().equalsIgnoreCase(enterprise.getName())) {
                    for (WorkRequest request : network.getWorkQueue().getWorkRequestList()) {
                        if (request instanceof CSRActivityWorkRequest) {
                            csrWorkReq = (CSRActivityWorkRequest) request;
                            if (csrWorkReq.getStatus().equals("Pending")) {
                                Object[] row = new Object[7];
                                row[0] = csrWorkReq;
                                row[1] = csrWorkReq.getDescription();
                                row[2] = csrWorkReq.getNumberOfVolunteersRequired();
                                row[3] = csrWorkReq.getNumberOfVolunteersAccepted();
                                row[4] = csrWorkReq.getMoneyOffered();
                                row[5] = csrWorkReq.getEnterpriseName();
                                row[6] = csrWorkReq.getStatus();
                                model.addRow(row);

                            }
                        }
                    }
                }
            }
        }
    }

    public void populateAssignedTable() {

        DefaultTableModel model = (DefaultTableModel) workAssignedJTable.getModel();

        model.setRowCount(0);
        CSRActivityWorkRequest csrWorkReq1;
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {

            csrWorkReq1 = (CSRActivityWorkRequest) request;
            for (UserAccount user : csrWorkReq1.getVolunteersList()) {
                if (user.equals(userAccount)) {
                    Object[] row = new Object[5];
                    row[0] = csrWorkReq1;
                    row[1] = csrWorkReq1.getDescription();
                    row[2] = csrWorkReq1.getMoneyOffered();
                    row[3] = csrWorkReq1.getEnterpriseName();
                    row[4] = csrWorkReq1.getStatus();
                    model.addRow(row);
                }
            }

        }

    }


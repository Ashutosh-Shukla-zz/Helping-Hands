    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (WorkRequest request : network.getWorkQueue().getWorkRequestList()) {
                if (request instanceof ResourcesWorkRequest) {
                    ResourcesWorkRequest res = (ResourcesWorkRequest) request;
                    if (res.getRaisedByAccount().equals(system.getCurrentUserAccount())) {
                        Object[] row = new Object[5];
                        row[0] = res.getRequsetId();
                        row[1] = res;
                        row[2] = res.getRaisedByAccount();
                        row[3] = res.getStatus();
                        row[4] = "";
                        for (Resource resource : res.getResourceRequested()) {

                            row[4] += resource.getName() + ", ";
                        }
                        model.addRow(row);
                    }
                }
            }
        }

    }

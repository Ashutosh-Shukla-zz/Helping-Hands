    private void viewDetailsButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewDetailsButtonMouseClicked

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select row");
            return;
        }

        ResourcesWorkRequest request = (ResourcesWorkRequest) workRequestJTable.getValueAt(selectedRow, 1);
        if (request.getStatus().equalsIgnoreCase("Completed")) {
            JOptionPane.showMessageDialog(null, "The request has been complted, Thank you for your support!");
        } else {
            //request.setStatus("Processing");
            ViewMappingRequestDetailsJPanel viewRequestDetailsJPanel = new ViewMappingRequestDetailsJPanel(userProcessContainer, request, system);
            userProcessContainer.add("viewRequestDetailsJPanel", viewRequestDetailsJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_viewDetailsButtonMouseClicked

    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (WorkRequest request : network.getWorkQueue().getWorkRequestList()) {
                if (request instanceof ResourcesWorkRequest) {
                    ResourcesWorkRequest res = (ResourcesWorkRequest) request;
                    Object[] row = new Object[5];
                    row[0] = res.getRequsetId();
                    row[1] = res;
                    row[2] = res.getRaisedByAccount();
                    row[3] = res.getStatus();
                    row[4] = "";
                    for (Resource resource : res.getResourceRequested()) {

                        row[4] += resource.getName() + ", ";
                    }
                    model.addRow(row);
                }
            }
        }

    }



    private void viewDetailedResourceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewDetailedResourceMouseClicked
        // TODO add your handling code here:

        int selectedRow = manageResourceJTable.getSelectedRow();

        if (selectedRow < 0) {

            return;
        }

        Resource resource = null;
        if (manageResourceJTable.getValueAt(selectedRow, 0) instanceof EducationResource) {
            resource = (EducationResource)manageResourceJTable.getValueAt(selectedRow, 0);
        } else if (manageResourceJTable.getValueAt(selectedRow, 0) instanceof FoodResource) {
            resource = (FoodResource)manageResourceJTable.getValueAt(selectedRow, 0);
        } else if (manageResourceJTable.getValueAt(selectedRow, 0) instanceof HealthResource) {
            resource = (HealthResource)manageResourceJTable.getValueAt(selectedRow, 0);
        } else if (manageResourceJTable.getValueAt(selectedRow, 0) instanceof MoneyResource) {
            resource = (MoneyResource)manageResourceJTable.getValueAt(selectedRow, 0);
        }

        ViewDetailResourceJpanel viewDetailResourceJpanel = new ViewDetailResourceJpanel(userProcessContainer, resource, system);
        userProcessContainer.add("viewDetailResourceJpanel", viewDetailResourceJpanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_viewDetailedResourceMouseClicked


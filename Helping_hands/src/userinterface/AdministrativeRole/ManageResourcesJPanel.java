    public void populateData() {

        DefaultTableModel model = (DefaultTableModel) manageResourceJTable.getModel();

        model.setRowCount(0);

        for (Map.Entry<String, List<Resource>> resourceList : resourcesOffered.entrySet()) {
            for (Resource resources : resourceList.getValue()) {
                Object row[] = new Object[3];
                row[0] = resources;
                row[1] = resources.getCreatedDate();
                row[2] = resources.getExpiryDate();
                ((DefaultTableModel) manageResourceJTable.getModel()).addRow(row);
            }
        }
    }


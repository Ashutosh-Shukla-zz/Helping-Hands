    private void viewDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select row");
            return;
        }

        ResourcesWorkRequest request = (ResourcesWorkRequest) workRequestJTable.getValueAt(selectedRow, 1);
        if (request.getStatus().equalsIgnoreCase("Completed")) {
            JOptionPane.showMessageDialog(null, "The Request is already completed, Thank you for your support!");
        } else {
            Boolean test = Boolean.FALSE;
            for (Resource resource : request.getResourceRequested()) {
                if (enterprise.getResourcesOffered().keySet().contains(resource.getName())) {
                    test = Boolean.TRUE;
                }
            }
            if (test) {
                ViewOpenRequestDetailsJPanel viewOpenRequestDetailsJPanel = new ViewOpenRequestDetailsJPanel(userProcessContainer, request, system, enterprise);
                userProcessContainer.add("viewOpenRequestDetailsJPanel", viewOpenRequestDetailsJPanel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            } else {
                JOptionPane.showMessageDialog(null, "You dont provide resource of this type, Thank you for your support");
            }
        }
    }//GEN-LAST:event_viewDetailsButtonActionPerformed

